/*=========================================================================*/
void H_DCMotor_vInit(void)
{
	MGPIO_vSetPinMode(MOTORS_PORT, DC_MOTOR_PIN,OUTPUT);
	MGPIO_vSetPinOutSpeed(MOTORS_PORT,DC_MOTOR_PIN,VERY_HIGH_SPEED);
}


/*=========================================================================*/
/*=========================================================================*/
void H_DCMotor_vStop(void)
{

	MGPIO_vWriteData(MOTORS_PORT, DC_MOTOR_PIN, LOW);
}

/*=========================================================================*/
/*=========================================================================*/
void H_DCMotor_vMove(void)
{
	MGPIO_vWriteData(MOTORS_PORT, DC_MOTOR_PIN, HIGH);
}

